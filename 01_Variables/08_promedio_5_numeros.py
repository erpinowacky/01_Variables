'''Programa que permita ingresar 5 números y calcular el promedio.'''
print('Calcular promedio de cinco números enteros\n')
num1=int(input('Digite el primer número entero: '))
num2=int(input('Digite el segundo número entero: '))
num3=int(input('Digite el tercer número entero: '))
num4=int(input('Digite el cuarto número entero: '))
num5=int(input('Digite un quinto entero: '))
promedio=(num1+num2+num3+num4+num5)/5
print('El promedio de estos cinco números es: ',promedio)